package eu.unicore.services.ws.cxf;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.cxf.transport.servlet.CXFNonSpringServlet;

public class Servlet extends CXFNonSpringServlet{

	private static final long serialVersionUID = 1L;
	
	private static final ThreadLocal<HttpServletRequest>requests=new ThreadLocal<HttpServletRequest>();
	
	@Override
	protected void invoke(HttpServletRequest request, HttpServletResponse response)throws ServletException{
		try{
			requests.set(request);
			super.invoke(request, response);
		}finally{
			requests.remove();
		}
	}
	
	/**
	 * get the current thread's {@link HttpServletRequest} (using a thread-local)
	 * @return the current thread's {@link HttpServletRequest}
	 */
	public static final HttpServletRequest getCurrentRequest(){
		return requests.get();
	}
}